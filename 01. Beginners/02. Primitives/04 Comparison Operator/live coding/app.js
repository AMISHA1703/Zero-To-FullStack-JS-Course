// ***********************************
// RELATIONAL OPERATORS
// > Greater Then
// < Less Then
// >= Greater Then or equal to
// <= Less Then or equal to

console.log(10 > 10);        // o/p: false
console.log(10 < 10);       // o/p: false
console.log(10 >= 10);     // o/p: true
console.log(10 <= 10);    // o/p: true

